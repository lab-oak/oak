std::string container_string(const auto &v) {
  std::stringstream ss{};
  for (auto x : v) {
    if constexpr (std::is_same_v<decltype(x), std::string>) {
      ss << x << ' ';
    } else {
      ss << std::to_string(x) << ' ';
    }
  }
  return ss.str();
}

